#!/usr/bin/env node
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function flattenKeys(obj, prefix = "") {
  /** @type {string[]} */
  const keys = [];
  for (const [k, v] of Object.entries(obj)) {
    const p = prefix ? `${prefix}.${k}` : k;
    if (v !== null && typeof v === "object" && !Array.isArray(v)) {
      keys.push(...flattenKeys(v, p));
    } else {
      keys.push(p);
    }
  }
  return keys;
}

const enPath = path.join(root, "src/locales/en.json");
const en = JSON.parse(fs.readFileSync(enPath, "utf8"));
const keys = flattenKeys(en).sort();
const unique = [...new Set(keys)];

const header = `/** Generated by scripts/gen-locale-keys.mjs — do not edit by hand */\n`;
const typeLine = `export type LocaleKey =\n  | ${unique.map((k) => JSON.stringify(k)).join("\n  | ")};\n`;
const constLine = `export const LOCALE_KEYS: readonly LocaleKey[] = [\n${unique.map((k) => `  ${JSON.stringify(k)}`).join(",\n")},\n];\n`;

const outDir = path.join(root, "src/generated");
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(path.join(outDir, "locale-keys.ts"), header + typeLine + constLine);
console.log(`Wrote ${unique.length} keys to src/generated/locale-keys.ts`);
